package BatchNew_Prectice;

public class switch_Statement {

	public static void main(String[] args) {

		String color = "yellow";

		switch (color) {
		case "White":

			System.out.println("Color is white");
			break;

		case "yellow":

			System.out.println("color is yellow");

			break;

		default:

			System.out.println("Invalid color");
			break;
		}
	}

}
